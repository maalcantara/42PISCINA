#include "utils.h"

int	ft_atoi(t_string str)
{
	int	i;
	int	val;

	i = 0;
	val = 0;
	while (str[i] != '\0')
	{
		val = val * 10 + (str[i] - '0');
		i++;
	}
	return (val);
}
